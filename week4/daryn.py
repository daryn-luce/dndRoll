def add_num(num1,num2):
    return num1 + num2

def sub_num(num1,num2):
    return num1 - num2
    
def mult_num(num1,num2):
    return num1 * num2
    
def div_num(num1,num2):
    return num1 / num2

def cube_num(num):
    return num**3
    
def sqr_num(num):
    return num**2

def sqrt_num(num):
    return num**(.5)
    
